package Abstracta;

public class Cliente {
    private int numeroCliente;
    private String apellido;
    private String dni;
    private String cuit;

    public Cliente(int numeroCliente, String apellido, String dni, String cuit) {
        this.numeroCliente = numeroCliente;
        this.apellido = apellido;
        this.dni = dni;
        this.cuit = cuit;
    }

}

